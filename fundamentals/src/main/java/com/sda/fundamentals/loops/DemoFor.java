package com.sda.fundamentals.loops;

public class DemoFor {

    public static void main(String[] args) {
        // 0 1 2 3 4 5 - known number of operations

        // initial counter ; final condition; step
        // from ; to ; step
        for (int i = 0; i <= 5; i++) {
            System.out.println("i = " + i);
        }
    }
}
