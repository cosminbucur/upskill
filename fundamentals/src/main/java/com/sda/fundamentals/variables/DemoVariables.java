package com.sda.fundamentals.variables;

public class DemoVariables {

    // declare variable
    // type name
    // definition
    int x;

    // initialize variable
    int y = 1;

    public static void main(String[] args) {
        // integer types

        // 1 byte -128 to 127 | default 0
        byte aByte = 127;

        // 2 byte -32768 to 32767  | default 0
        short aShort = 32767;

        // 4 bytes | default 0
        int anInt = 2147483647;

        // 2^63 | default 0L
        long aLong = 2147483647L;

        // float point types
        float aFloat = 18.1234567f;

        // double
        double aDouble = 20.5;

        // logical type
        // default false
        boolean aBoolean = true;

        // default ''
        char myLetter = 'a';

        String someText = "java code";
    }
}
